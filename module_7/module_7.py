a = 'hello'
chars = []
for i in a:
    chars.append(ord(i))
print(chars)

s = ''
for i in chars:
    s += chr(i) #  Получение символа из кода
print(s)

for i in range(1000, 1300):
    print(chr(i), end=' ')

