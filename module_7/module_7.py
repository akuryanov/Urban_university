'''
Байты — это представление строкового символа или строки целиком в двоичном или шестнадцатеричном представлении. 
Для работы с байтами существует отдельный тип данных. Байты выглядят как обычная строка, 
но с префиксом «b» перед строкой. После префикса может быть что угодно. 
Например, давайте переведём символ «h» в его числовое представление с помощью функции «ord», 
а затем преобразуем это представление в шестнадцатеричный формат с помощью функции «hex»
'''

print(hex(ord('h')))
bb = b'\x68'
print(type(bb))
print(bb.decode())